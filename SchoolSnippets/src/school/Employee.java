package school;

public class Employee {
private int num;
private String name;
private String type;

public int getNum()
{
	return this.num;
}

public String getName()
{
	return this.name;
}

public String getType()
{
	return this.type;
}

public void setNum(int n)
{
	this.num = n;
}

public void setName(String s)
{
	this.name = s;
}

public void setType(String t)
{
	this.type = t;
}
}
